package ua.lviv.lgsa.auto;

public class Steering {
	private int diameterSteering;
	
	public void setDiamSt(int diam) {
		
		this.diameterSteering = diam;
	}
	
	public int getDiamSt() {
		return this.diameterSteering;
	}

	public String toString() {
		return "Steering [diameterSteering=" + diameterSteering + "]";
	}
	
	

}
